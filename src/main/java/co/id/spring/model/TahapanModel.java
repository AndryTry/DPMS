package co.id.spring.model;

public class TahapanModel {

	private int idTahapan;
	private String namaTahapan;
	private int isActive;
	
	public int getIdTahapan() {
		return idTahapan;
	}
	
	public void setIdTahapan(int idTahapan) {
		this.idTahapan = idTahapan;
	}
	
	public String getNamaTahapan() {
		return namaTahapan;
	}
	
	public void setNamaTahapan(String namaTahapan) {
		this.namaTahapan = namaTahapan;
	}
	
	public int getIsActive() {
		return isActive;
	}
	
	public void setIsActive(int isActive) {
		this.isActive = isActive;
	}
		
}